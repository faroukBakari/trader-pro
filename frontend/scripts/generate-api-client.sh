    echo ""
    echo -e "${RED}â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•${NC}"
    echo -e "${RED}âŒ Failed to generate client from API specifications${NC}"
    echo -e "${RED}â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•${NC}"
    echo ""
    echo -e "${BLUE}â„¹ï¸  Reasons this might happen:${NC}"
    echo -e "   â€¢ Backend specification export failed"
    echo -e "   â€¢ Client generation tools failed"
    echo -e "   â€¢ Missing dependencies"
    echo ""
    exit 1
}

# Run main function
main
