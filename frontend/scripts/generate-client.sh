#!/bin/bash
# Frontend API Client Generator
# This script attempts to generate the TypeScript client from a live API server.
# If the API is not available, it falls back to using the mock client implementation.

set -e

# Configuration
API_URL="${VITE_API_URL:-http://localhost:${BACKEND_PORT:-8000}}"
OUTPUT_DIR="./src/services/generated"
OPENAPI_SPEC="openapi.json"
CLIENT_PACKAGE_NAME="@trading-api/client"

# Colors for output
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
RED='\033[0;31m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

echo -e "${BLUE}ðŸš€ Frontend API Client Generator${NC}"
echo ""

# Clean up previous generated client
echo -e "${BLUE}ðŸ§¹ Cleaning previous generated client...${NC}"
rm -rf "$OUTPUT_DIR"
mkdir -p "$OUTPUT_DIR"
echo -e "${GREEN}âœ… Cleanup complete${NC}"
echo ""

# Function to check if API server is available
check_api_available() {
    local health_url="$API_URL/api/v1/health"
    echo -e "${BLUE}ðŸ“¡ Checking API server at: $health_url${NC}"

    if curl -sf "$health_url" > /dev/null 2>&1; then
        echo -e "${GREEN}âœ… API server is available${NC}"
        return 0
    else
        echo -e "${YELLOW}âš ï¸  API server is not available${NC}"
        return 1
    fi
}

# Function to download OpenAPI specification
download_openapi_spec() {
    # First, try to use local file if it exists (for file watching mode)
    local local_openapi="../backend/openapi.json"
    if [ -f "$local_openapi" ]; then
        echo -e "${BLUE}ðŸ“ Using local OpenAPI specification: $local_openapi${NC}"
        cp "$local_openapi" "$OPENAPI_SPEC"
        echo -e "${GREEN}âœ… Local OpenAPI specification copied${NC}"
        return 0
    fi

    # Fallback to downloading from server
    local openapi_url="$API_URL/api/v1/openapi.json"
    echo -e "${BLUE}ðŸ“¥ Downloading OpenAPI specification from: $openapi_url${NC}"

    if curl -sf "$openapi_url" -o "$OPENAPI_SPEC"; then
        echo -e "${GREEN}âœ… OpenAPI specification downloaded${NC}"
        return 0
    else
        echo -e "${RED}âŒ Failed to download OpenAPI specification${NC}"
        return 1
    fi
}

# Function to generate TypeScript client
generate_client() {
    echo -e "${BLUE}ðŸ”§ Generating TypeScript client...${NC}"

    # Generate client using openapi-generator
    if npx @openapitools/openapi-generator-cli generate \
        -i "$OPENAPI_SPEC" \
        -g typescript-axios \
        -o "$OUTPUT_DIR" \
        --additional-properties="\
npmName=$CLIENT_PACKAGE_NAME,\
supportsES6=true,\
typescriptThreePlus=true,\
withInterfaces=true,\
withSeparateModelsAndApi=true,\
apiPackage=api,\
modelPackage=models,\
useSingleRequestParameter=true\
" > /dev/null 2>&1; then

        echo -e "${GREEN}âœ… Client generation successful${NC}"

        # Create index file for easy imports
        create_index_file

        # Create configuration wrapper
        create_config_wrapper

        # Create client type indicator
        echo "server" > "$OUTPUT_DIR/.client-type"

        return 0
    else
        echo -e "${RED}âŒ Client generation failed${NC}"
        return 1
    fi
}

# Function to create index file
create_index_file() {
    cat > "$OUTPUT_DIR/index.ts" << 'EOF'
// Auto-generated API client exports
// This file is automatically generated. Do not edit manually.

// API Classes
export * from './api'

// Models/Types
export * from './models'

// Base configuration
export * from './base'
export * from './common'
export * from './configuration'

// Re-export commonly used types
export type {
  AxiosResponse,
  AxiosError,
  AxiosRequestConfig
} from 'axios'
EOF
}

# Function to create configuration wrapper
create_config_wrapper() {
    cat > "$OUTPUT_DIR/client-config.ts" << 'EOF'
// Trading API Client Configuration
// This file provides a pre-configured client instance

import { Configuration } from './configuration'
import { HealthApi, VersioningApi } from './api'

// API Configuration - using empty basePath for same-origin requests
const apiConfig = new Configuration({
  basePath: '',
})

// Pre-configured API client instances
export const healthApi = new HealthApi(apiConfig)
export const versioningApi = new VersioningApi(apiConfig)

// Export configuration for custom instances
export { apiConfig, Configuration }

// Export all API classes
export * from './api'
export * from './models'
EOF
}

# Function to setup mock client fallback
setup_mock_fallback() {
    echo -e "${YELLOW}ðŸŽ­ Setting up mock client fallback...${NC}"

    # Ensure directory exists for mock mode
    mkdir -p "$OUTPUT_DIR"

    # Create a marker file to indicate mock mode
    echo "mock" > "$OUTPUT_DIR/.client-type"

    cat > "$OUTPUT_DIR/README.md" << 'EOF'
# Mock API Client

The generated API client is not available because the backend API server is not running.

The application will automatically use mock data from `apiService.ts` for development and testing.

## To generate the real client:

1. Start the backend API server:
   ```bash
   cd ../backend
   make dev
   ```

2. Re-run client generation:
   ```bash
   npm run client:generate
   ```

## Manual generation:

```bash
./scripts/generate-client.sh
```

The app will work perfectly fine with mock data for development!
EOF

    echo -e "${GREEN}âœ… Mock client fallback configured${NC}"
    echo -e "${YELLOW}ðŸ“ The app will use mock data from apiService.ts${NC}"
}

# Main execution
main() {
    echo ""

    if check_api_available; then
        echo ""
        if download_openapi_spec; then
            echo ""
            if generate_client; then
                echo ""
                echo -e "${GREEN}ðŸŽ‰ Success! Generated client from live API${NC}"
                echo -e "${GREEN}ðŸ“ Client location: $OUTPUT_DIR${NC}"
                echo -e "${GREEN}ðŸ”§ Import in your components:${NC}"
                echo -e "   import { healthApi, versioningApi } from '@/services/generated/client-config'"
                echo ""

                # Cleanup
                rm -f "$OPENAPI_SPEC"
                exit 0
            fi
        fi
    fi

    echo ""
    echo -e "${YELLOW}â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•${NC}"
    echo -e "${YELLOW}âš ï¸  Could not generate client from live API${NC}"
    echo -e "${YELLOW}â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•${NC}"
    echo ""
    echo -e "${BLUE}â„¹ï¸  Reasons this might happen:${NC}"
    echo -e "   â€¢ Backend API server is not running"
    echo -e "   â€¢ API server is on a different URL"
    echo -e "   â€¢ Network connectivity issues"
    echo ""
    echo -e "${BLUE}ðŸ’¡ What happens now:${NC}"
    echo -e "   â€¢ App will use ${GREEN}mock data${NC} from apiService.ts"
    echo -e "   â€¢ All features will work for development"
    echo -e "   â€¢ You can generate later when API is ready"
    echo ""
    echo -e "${BLUE}ðŸš€ To generate from live API:${NC}"
    echo -e "   1. Start backend: ${GREEN}cd backend && make dev${NC}"
    echo -e "   2. Run: ${GREEN}npm run client:generate${NC}"
    echo ""

    setup_mock_fallback

    echo ""
    echo -e "${GREEN}âœ… Setup complete - using mock client${NC}"
    echo ""
}

# Run main function
main
